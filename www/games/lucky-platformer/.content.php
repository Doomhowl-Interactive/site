<p>
    Lucky Platformer is a frustrating platform game made in less than 48 hours
    for the sixth annual GMTK game jam with the theme "Roll of the Dice." There
    are 10 levels with a boss at the end. You can play the game on itch.io.
</p>

<p>
    This game is not only a test of your patience and skill, but also your
    luck!
</p>

<p>
    Each level is scattered with Lucky Blocks, (inspired by the Minecraft mod),
    which will give you random status effects and will edit the level at random.
</p>

<h2>Background</h2>
<p>
    The game is heavily inspired by rage games like The World's Hardest Game.
    Imagine all the annoying things about these games, but worse. The controls
    are weird, the physics are all over the place, the level design is
    non-existent and the graphics are bad, but what can you do in 48 hours?
</p>
<p>
    Lucky Platformer is completely programmed in Java using the LibGDX framework
    with my custom implementation called MagmaEngine slapped on top.
</p>
<p>
    This is my first game-jam submission ever. Even though it falls short in
    many areas, I had a lot of fun making the game!
</p>

<div class="alert alert-info">
    <strong>Note:</strong> Make sure to disable browser extensions like Vimium, so your
    keybindings don't get remapped.
</div
>

